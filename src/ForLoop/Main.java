package ForLoop;

public class Main {

	public static void main(String[] args) {
		
		for(int i = 0; i < 1; i++) { //printing it 9x
			System.out.println("i love dogs");
		}
		
		for(int i = 0; i < 1; i++) {
			System.out.println(i);
		}
		
		int[] grades = {90,100,80,93};
		
		for(int i = 0; i <grades.length; i ++) {
			System.out.println(i + " grades are " + grades[i]);
		}
		
		
	}

}
